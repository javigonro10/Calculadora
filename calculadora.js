// calculadora.js

function sumar(a, b) {
    return a + b;
  }
  
  function restar(a, b) {
    return a - b;
  }

  function multiplicar(a, b) {
    return a * b;
  }

  function dividir(a, b) {
    return a / b;
  }

  function c_Kelvin(a){
    return a + 273
  }


  module.exports = { sumar, restar, multiplicar , dividir , c_Kelvin};